// ランダムに1件取得する関数
export function getRandomKobanashi(characterId, rank) {
  const lines = KOBANASHI[characterId]?.[rank];
  if (!lines || lines.length === 0) return null;
  return lines[Math.floor(Math.random() * lines.length)];
}
